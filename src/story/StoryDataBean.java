package story;

public class StoryDataBean {
	private int story_num;
	private String story_member_id;
	private String story_title;
	private String story_writer;
	private String story_content;
	public int getStory_num() {
		return story_num;
	}
	public void setStory_num(int story_num) {
		this.story_num = story_num;
	}
	public String getStory_member_id() {
		return story_member_id;
	}
	public void setStory_member_id(String story_member_id) {
		this.story_member_id = story_member_id;
	}
	public String getStory_title() {
		return story_title;
	}
	public void setStory_title(String story_title) {
		this.story_title = story_title;
	}
	public String getStory_writer() {
		return story_writer;
	}
	public void setStory_writer(String story_writer) {
		this.story_writer = story_writer;
	}
	public String getStory_content() {
		return story_content;
	}
	public void setStory_content(String story_content) {
		this.story_content = story_content;
	}
	
	
	
}
